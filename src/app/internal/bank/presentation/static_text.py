def transaction_history(history, account) -> str:
    if len(history) == 0:
        raise ValueError

    text = account_history
    last_date = None
    for transaction in history:
        date = transaction.date.strftime('%b %d')
        if last_date != date:
            last_date = date
            text += f'[ {last_date} ]\n'

        amount = transaction.amount
        if amount == int(amount):
            amount = int(amount)

        if str(transaction.to_account.number) == account:
            text += f"‚û°Ô∏è <b>–í—Ö–æ–¥—è—â–∏–π –ø–µ—Ä–µ–≤–æ–¥</b> + {amount}‚ÇΩ\n" \
                    f"      –û—Ç - {transaction.to_account.owner.name}, "
        else:
            text += f"üí≥ <b>–ò—Å—Ö–æ–¥—è—â–∏–π –ø–µ—Ä–µ–≤–æ–¥</b> {amount}‚ÇΩ\n" \
                    f"      –ö–æ–º—É - {transaction.to_account.owner.name}, "

        text += f'–í—Ä–µ–º—è - {transaction.date.strftime("%H:%M")}\n\n'

    return text
